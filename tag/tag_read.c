static TAG_INFO_T s_tag_info[TOTAL_TAG_NUM] = {0};


void set_cur_tag_id(uint8_t id)
{
    s_cur_tag_id = id;
}

static void pwdAuth(TAG_INFO_T *pinfo)
{
  uint8_t raw_data[8];
  DesEncrypt8((const DES_key_schedule*)(s_static_pwd_key_schedule), pinfo->tag_uid, raw_data);
  
  // set pack
  ISO14443A_sendT2TPwdAuth(raw_data, s_static_pack);
}

static void DecryptTagInfo(TAG_INFO_T *pinfo)
{
  uint8_t *ptemp;
  uint8_t raw_data[16];
  // read data start from buf[1]
  DesDecrypt16((const DES_key_schedule*)(s_static_info_key_schedule), &g_pui8TrfBuffer[0], raw_data);
  memcpy(pinfo->filter_uid, raw_data, FILTER_UID_LEN);
  ptemp = raw_data + FILTER_UID_LEN;
  pinfo->year = (*ptemp << 8) | *(ptemp+1);
  ptemp += 2;
  pinfo->month = *ptemp++;
  pinfo->day = *ptemp++;
  pinfo->manufacturer = *ptemp++;
  pinfo->filter_type = *ptemp++;
}

static void DecryptAuthInfo(TAG_INFO_T *pinfo)
{
  uint8_t i, j;
  uint8_t raw_data[16];
  // read data start from buf[1]
  DesDecrypt16((const DES_key_schedule*)(s_static_auth_key_schedule), &g_pui8TrfBuffer[0], raw_data);
  
  pinfo->is_auth = 1;
  for (i = 0, j = 0; i < 8; i++, j += 2) 
  {
    if (pinfo->tag_uid[i] != (raw_data[j] ^ raw_data[j+1])) 
    {
        pinfo->is_auth = 0;
        break;
    }
  }
}

uint8_t parse_tag_info(void)
{
    TAG_INFO_T *pinfo;

    if (s_cur_tag_id >= TOTAL_TAG_NUM) {
        return 0;
    }

    pinfo = &s_tag_info[s_cur_tag_id];

    // 1. read TAG UID.
    ISO14443A_sendT2TReadFourBlocks(TAG_UID_START_BLOCK);
    //same uid and the tag is authed last time, discard this loop.
    if(pinfo->is_auth)
    {
	  	LED_14443A_ON;
	    return 1;
    }
    pinfo->tag_uid[1] = 0;
    memcpy(&pinfo->tag_uid[1], &g_pui8TrfBuffer[0], 3);
    memcpy(&pinfo->tag_uid[4], &g_pui8TrfBuffer[4], 4);
#ifdef TAG_USE_PWD_AUTH
    pwdAuth(pinfo);
#endif

    // 2. read TAG info.
    ISO14443A_sendT2TReadFourBlocks(TAG_INFO_START_BLOCK);
    DecryptTagInfo(pinfo);
    //check year and month
    if((((pinfo->filter_uid[0] >> 4) + 2017) != pinfo->year) || ((pinfo->filter_uid[0] & 0x0F) != pinfo->month))
    {
            pinfo->filter_type = 0x04; //0xFF; // invalid filter
            pinfo->is_auth = 0x00;
            return 2;
    }


    // 3. read AUTH info from TAG.
    ISO14443A_sendT2TReadFourBlocks(TAG_AUTH_START_BLOCK);
    DecryptAuthInfo(pinfo);


    if (pinfo->is_auth) 
    {
      LED_14443A_ON;
      return 1;
    } 
    pinfo->filter_type = 0x04; //0xFF; // invalid filter
    return 2;
}

const TAG_INFO_T * get_tag_info(uint8_t id)
{
    if (id >= TOTAL_TAG_NUM) {
        return NULL;
    } else {
        return &s_tag_info[id];
    }
}

int8_t clear_tag_info(uint8_t id)
{
    TAG_INFO_T *pinfo;

    if (id >= TOTAL_TAG_NUM) {
        return -1;
    } else {
        pinfo = &s_tag_info[id];
        memset(pinfo, 0x00, sizeof(TAG_INFO_T));
        pinfo->filter_type = 0xFF; // no tag found
    }

    return 0;
}

